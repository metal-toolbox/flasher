// CopyDevice returns a pointer to a copy of the given device object
func CopyInventory(src *common.Device) *common.Device {
	dst := &common.Device{}

	copyOptions := copier.Option{IgnoreEmpty: true, DeepCopy: true}

	err := copier.CopyWithOption(&dst, &src, copyOptions)
	if err != nil {
		panic(err)
	}

	return dst
}

